# Afficher chaque caractère d'une chaîne
chaine = "Bonjour"
for caractere in chaine:
    print(caractere)

# Afficher chaque élément d'une liste
liste = [1, 2, 3, 4, 5]
for element in liste:
    print(element)

# Afficher les entiers de 0 à 15 non compris, de trois en trois
for i in range(0, 15, 3):
    print(i)
