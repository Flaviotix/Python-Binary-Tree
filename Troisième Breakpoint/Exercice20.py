# Afficher les entiers de 0 à 3
for i in range(4):
    print(i)

# Afficher les entiers de 4 à 7
for i in range(4, 8):
    print(i)

# Afficher les entiers de 2 à 8 par pas de 2
for i in range(2, 9, 2):
    print(i)

